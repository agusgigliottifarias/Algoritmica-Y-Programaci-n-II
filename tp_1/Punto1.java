package tp_1;


public class Punto1 {

	public static int sumarNumeros (int number1, int number2) {
		return number1 + number2;
	}
	
	public static int productoNumeros (int number1, int number2) {
		return number1 * number2;	
	}
	public static int restaNumeros (int number1, int number2) {
		return number1 - number2;
	}
	
	public static float dividirNumeros (float number1, float number2) {
		return number1 / number2;
	}

}
